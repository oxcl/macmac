const SUPPORTED_LANGS = [
  { code: 'en', label: 'English' },
  { code: 'zh', label: '中文' },
  { code: 'ru', label: 'Русский' },
  { code: 'es', label: 'Español' },
  { code: 'fr', label: 'Français' },
  { code: 'pt', label: 'Português' },
  { code: 'ar', label: 'العربية' },
  { code: 'hi', label: 'हिन्दी' },
  { code: 'de', label: 'Deutsch' },
  { code: 'fa', label: 'فارسی' },
];

let currentLang = 'en';

function matchLang(code: string): string {
  const base = code.toLowerCase().split('-')[0];
  if (translations[base]) return base;
  return 'en';
}

export async function initLanguage(): Promise<void> {
  const stored = await StorageService.language.getValue();
  if (stored && translations[stored]) {
    currentLang = stored;
  } else {
    currentLang = matchLang(navigator.language);
  }
  document.documentElement.lang = currentLang;
  document.documentElement.dir = currentLang === 'ar' || currentLang === 'fa' ? 'rtl' : 'ltr';
}

export function t(key: string, ...args: string[]): string {
  let str = translations[currentLang]?.[key] ?? translations.en[key] ?? key;
  for (let i = 0; i < args.length; i++) {
    str = str.replace(`$${i + 1}`, args[i]);
  }
  return str;
}

export async function setLanguage(lang: string): Promise<void> {
  if (!translations[lang]) return;
  currentLang = lang;
  await StorageService.language.setValue(lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' || lang === 'fa' ? 'rtl' : 'ltr';
}

export function getLanguage(): string {
  return currentLang;
}

export function getSupportedLanguages() {
  return SUPPORTED_LANGS;
}

export function applyTranslations(): void {
  document.querySelectorAll<HTMLElement>('[data-i18n]').forEach((el) => {
    const key = el.dataset.i18n!;
    el.textContent = t(key);
  });
  document.querySelectorAll<HTMLElement>('[data-i18n-title]').forEach((el) => {
    const key = el.dataset.i18nTitle!;
    el.title = t(key);
  });
}
